export const excerpt = (text) => {
    return text.slice(0, process.env.REACT_APP_EXCEPTIONS) + "..."
}

export const find_cart_info = (cart) => {
    let newcart = []
    cart.forEach((valc, i) => {
        let info = data.filter((val) => val.id === valc.id)
        if (info) {
            newcart.push({ ...info[0], "quantity": valc.quantity })
        }
    })
    return newcart;
}
export const subcart_total = (total) => {
    const subTotal = total.reduce((prev, curr) => parseInt(prev) + (parseInt(curr.price) * parseInt(curr.quantity)), 0)
    return subTotal;
}